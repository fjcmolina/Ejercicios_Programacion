/**
 * Sentencia Condicional
 *
 * 3. Muestra los numeros multiplos de 5 de 0 a 100 utilizando un bucle do-while.
 */
 
 public class Ejercicio3{
  public static void main(String[] args) {
    
    System.out.println("Vamos a mostrar los numeros multiplos de 5");
    
    int num = 0;
    
    do{
      System.out.println(num);
      num += 5;
      }
    while(num <= 100);
  }
  
}

