/**
 * Sentencia Condicional
 *
 * 1. Muestra los numeros multiplos de 5 de 0 a 100 utilizando un bucle for.
 */
 
 public class Ejercicio1{
  public static void main(String[] args) {
    
    System.out.println("Vamos a mostrar los numeros multiplos de 5");
    
    int num;
    
    for( num = 0; num <= 100; num += 5){
      System.out.println(num);
      }
  }
}
